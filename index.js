// ===== GENERATOR =====
function generateMessage() {
    const emoji = random(["❤️","🥰","😏","💫","🔥","😘","✨","💛"]);
    const opener = random(["Солнышко,", "Эй, Солнышко 💛", "Моя хорошая,", "Слушай, Солнышко,"]);

    const compliment = random(compliments);

    return `${opener} ${compliment} ${emoji}`;
}

function random(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

// ===== BUTTONS =====
bot.on('message', (msg) => {
    const text = msg.text;
    if (!text) return;

    let response = null;

    if (text === "💖 Комплимент") response = generateMessage();
    if (text === "🥰 Нежность") response = generateMessage();
    if (text === "😏 Флирт") response = generateMessage();
    if (text === "🔥 Дерзкий") response = generateMessage();

    if (response) {
        bot.sendMessage(msg.chat.id, response);
    }
});

// ===== AUTO MESSAGE =====
cron.schedule('0 9 * * *', () => {
    if (!userChatId) return;

    bot.sendMessage(userChatId, generateMessage());
}, {
    timezone: "Asia/Almaty"
});
